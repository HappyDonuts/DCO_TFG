void resistencia(uint8_t valor){
	static int resist = 0;
	steps = 2*(valor - resist);

	if (steps < 0){
		HAL_GPIO_WritePin(GPIOB,GPIO_PIN_1, 0);
		steps = -steps;
	}
	else {
		HAL_GPIO_WritePin(GPIOB,GPIO_PIN_1, 1);
	}
	 HAL_TIM_Base_Start_IT(&htim6);
	 resist = valor;

}

